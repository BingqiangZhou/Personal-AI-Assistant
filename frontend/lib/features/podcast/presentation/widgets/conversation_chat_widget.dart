  Widget _buildMessageBubble(
    BuildContext context,
    PodcastConversationMessage message,
  ) {
    final isUser = message.isUser;
    final userTextColor = Theme.of(context).colorScheme.onSurface;
    final isSelected = _isMessageSelected(message);
    final roleLabel = _messageRoleLabel(message);

    return Align(
      alignment: isUser ? Alignment.centerRight : Alignment.centerLeft,
      child: Container(
        constraints: BoxConstraints(
          maxWidth: MediaQuery.of(context).size.width * 0.75,
        ),
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
        decoration: BoxDecoration(
          color: isUser
              ? Theme.of(context).colorScheme.primaryContainer
              : Theme.of(context).colorScheme.surfaceContainerHighest,
          borderRadius: BorderRadius.circular(16),
          border: Border.all(
            color: _isMessageSelectMode
                ? (isSelected
                      ? Theme.of(context).colorScheme.primary
                      : Theme.of(
                          context,
                        ).colorScheme.outlineVariant.withValues(alpha: 0.35))
                : (isUser
                      ? Theme.of(
                          context,
                        ).colorScheme.primary.withValues(alpha: 0.3)
                      : Theme.of(
                          context,
                        ).colorScheme.outlineVariant.withValues(alpha: 0.3)),
            width: _isMessageSelectMode && isSelected ? 1.6 : 1,
          ),
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Message header with role and time
            Row(
              mainAxisSize: MainAxisSize.min,
              children: [
                Icon(
                  isUser ? Icons.person_outline : Icons.smart_toy_outlined,
                  size: 14,
                  color: isUser
                      ? userTextColor
                      : Theme.of(context).colorScheme.onSurfaceVariant,
                ),
                const SizedBox(width: 4),
                Text(
                  roleLabel,
                  style: Theme.of(context).textTheme.labelSmall?.copyWith(
                    color: isUser
                        ? userTextColor
                        : Theme.of(context).colorScheme.onSurfaceVariant,
                    fontWeight: FontWeight.w600,
                  ),
                ),
                if (_isMessageSelectMode) ...[
                  const SizedBox(width: 6),
                  Icon(
                    isSelected
                        ? Icons.check_circle
                        : Icons.radio_button_unchecked,
                    size: 16,
                    color: isSelected
                        ? Theme.of(context).colorScheme.primary
                        : Theme.of(context).colorScheme.onSurfaceVariant,
                  ),
                ],
              ],
            ),
            const SizedBox(height: 6),
            GestureDetector(
              behavior: HitTestBehavior.opaque,
              onTap: _isMessageSelectMode
                  ? () => _toggleMessageSelection(message)
                  : null,
              child: _isMessageSelectMode
                  ? Text(
                      message.content,
                      style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                        color: isUser
                            ? userTextColor
                            : Theme.of(context).colorScheme.onSurface,
                        height: 1.5,
                      ),
                    )
                  : SelectableText(
                      message.content,
                      onSelectionChanged: (selection, _) {
                        _updateSelectedChatText(
                          message.content,
                          selection,
                          isUser: isUser,
                          roleLabel: roleLabel,
                        );
                      },
                      contextMenuBuilder: (context, editableTextState) {
                        return AdaptiveTextSelectionToolbar.buttonItems(
                          anchors: editableTextState.contextMenuAnchors,
                          buttonItems: [
                            ...editableTextState.contextMenuButtonItems,
                            ContextMenuButtonItem(
                              label: AppLocalizations.of(
                                context,
                              )!.podcast_share_as_image,
                              onPressed: () {
                                final value =
                                    editableTextState.textEditingValue;
                                _lastSelectedChatText = value.selection
                                    .textInside(value.text)
                                    .trim();
                                _lastSelectedChatRoleLabel = roleLabel;
                                _lastSelectedChatIsUser = isUser;
                                ContextMenuController.removeAny();
                                unawaited(_shareSelectedChatAsImage());
                              },
                            ),
                          ],
                        );
                      },
                      style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                        color: isUser
                            ? userTextColor
                            : Theme.of(context).colorScheme.onSurface,
                        height: 1.5,
                      ),
                    ),
            ),
          ],
        ),
      ),
    );
  }

  Widget _buildInputArea(BuildContext context, ConversationState state) {
    final l10n = AppLocalizations.of(context)!;
    return Container(
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: Theme.of(context).colorScheme.surface,
        border: Border(
          top: BorderSide(
            color: Theme.of(context).colorScheme.outlineVariant,
            width: 1,
          ),
        ),
      ),
      child: SafeArea(
        top: false,
        child: Row(
          children: [
            Expanded(
              child: TextField(
                controller: _messageController,
                focusNode: _focusNode,
                enabled: state.isReady && widget.aiSummary != null,
                style: Theme.of(context).textTheme.bodyMedium?.copyWith(
                  color: Theme.of(context).colorScheme.onSurface,
                ),
                cursorColor: Theme.of(context).colorScheme.primary,
                maxLines: null,
                minLines: 1,
                textInputAction: TextInputAction.send,
                onSubmitted: (_) => _sendMessage(),
                decoration: InputDecoration(
                  hintText: widget.aiSummary == null
                      ? l10n.podcast_conversation_no_summary_hint
                      : l10n.podcast_conversation_send_hint,
                  border: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(24),
                    borderSide: BorderSide(
                      color: Theme.of(context).colorScheme.outline,
                    ),
                  ),
                  enabledBorder: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(24),
                    borderSide: BorderSide(
                      color: Theme.of(context).colorScheme.outline,
                    ),
                  ),
                  focusedBorder: OutlineInputBorder(
                    borderRadius: BorderRadius.circular(24),
                    borderSide: BorderSide(
                      color: Theme.of(context).colorScheme.primary,
                      width: 2,
                    ),
                  ),
                  contentPadding: const EdgeInsets.symmetric(
                    horizontal: 20,
                    vertical: 12,
                  ),
                ),
              ),
            ),
            const SizedBox(width: 8),
            IconButton.filled(
              onPressed:
                  (state.isReady &&
                      _messageController.text.trim().isNotEmpty &&
                      widget.aiSummary != null)
                  ? _sendMessage
                  : null,
              icon: state.isSending
                  ? const SizedBox(
                      width: 20,
                      height: 20,
                      child: CircularProgressIndicator(
                        strokeWidth: 2,
                        valueColor: AlwaysStoppedAnimation<Color>(Colors.white),
                      ),
                    )
                  : const Icon(Icons.send),
              style: IconButton.styleFrom(padding: const EdgeInsets.all(12)),
            ),
          ],
        ),
      ),
    );
  }
